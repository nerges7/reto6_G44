    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
        if (action_performer) {
            if (txt_clinic.getText().isEmpty() || txt_address.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Falta ingresar el nombre del hospital o la dirección");
            } else {
                DataModel.addHospitals(txt_clinic.getText(), txt_address.getText());
                JOptionPane.showMessageDialog(this, "La clinica ha sido creada");
                this.setVisible(false);

            }
        }else{
            if (txt_clinic.getText().isEmpty() || txt_address.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Falta ingresar el nombre del hospital o la dirección");
            } else {
                int id = Integer.parseInt(txt_id.getText());
                DataModel.setHospitals(id, txt_clinic.getText(), txt_address.getText());
                JOptionPane.showMessageDialog(this, "La clinica ha sido editada con exito");
                this.setVisible(false);

            }
        }
    }//GEN-LAST:event_btn_saveActionPerformed

