    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        if (action_performer) {
            if (txt_pet.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Falta ingresar el nombre de la mascota");
            } else {
                String name = txt_pet.getText();
                String pet_type = cb_pet_type.getSelectedItem().toString();
                String race = txt_race.getText();
                DataModel.addPets(name, pet_type,race,idowners);
                JOptionPane.showMessageDialog(this, "La mascota fue creada con exito");
                this.setVisible(false);

            }
        }else {
            if (txt_pet.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Falta ingresar el nombre de la mascota");
            }else {
                int id = Integer.parseInt(txt_id_pet.getText());
                String name = txt_pet.getText();
                String pet_type = cb_pet_type.getSelectedItem().toString();
                String race = txt_race.getText();
                DataModel.setPets(id,name, pet_type,race, idowners);
                JOptionPane.showMessageDialog(this, "La mascota fue modificada con exito");
                this.setVisible(false);
            }
           
        }
    }//GEN-LAST:event_btn_saveActionPerformed

